// Runtime float type checker
const _isFloat = Object.freeze({
    // Floats
    f4:  (v) => typeof v === 'number' && Number.isFinite(v) && _isF4_LUT.has(v), // -6.0 to 6.0
    f6:  (v) => typeof v === 'number' && Number.isFinite(v) && _isF6_LUT.has(v), // -28.0 to 28.0
    f8:  (v) => typeof v === 'number' && Number.isFinite(v) && _isF8_LUT.has(v), // -480 to 480
    f16: (v) => { // Based on new ES2025 standard (w fallback)
        let m = Math, f = Float32Array;
        if (typeof v !== 'number' || !Number.isFinite(v) || m.abs(v) > 65504) return false; // Type & range
        if (typeof m.f16round === 'function') return m.f16round(v) === v; // Native ES2025
        if (typeof Float16Array === 'function') return new Float16Array([v])[0] === v; // TypedArray
        try { // DataView fallback
            let b = new ArrayBuffer(4), d = new DataView(b);
            if (typeof d.setFloat16 === 'function') {
                d.setFloat16(0, v);
                return d.getFloat16(0) === v;
            }
        } catch (e) {}
        let absV = m.abs(v); // Hardened bit-check & subnormal guard
        if (absV < 0.00006103515625) {
            let unit = 0.000000059604644775390625; // 2^-24 (f16 subnormal resolution)
            return m.round(v / unit) * unit === v;
        }
        let buf = new f([v]), u32 = new Uint32Array(buf.buffer);
        let bits = u32[0];
        if (buf[0] !== v) return false;
        return (bits & 0x1FFF) === 0;
    },
    f32: (v) => typeof v === 'number' && Number.isFinite(v) && Math.fround(v) === v,
    f64: (v) => typeof v === 'number' && Number.isFinite(v),
});